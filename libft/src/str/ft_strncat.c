#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t count)
{
	int		j;
	int		i;

	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	j = 0;
	while (s2[j] != '\0' && j < (int)count)
	{
		s1[j + i] = s2[j];
		j++;
	}
	s1[j + i] = '\0';
	return (s1);
}
