#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*res;

	res = (char*)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (res == NULL)
		return (NULL);
	return (ft_strcpy(res, s));
}
