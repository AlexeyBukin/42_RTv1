#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t		i;
	t_byte		*t1;
	t_byte		*t2;

	i = 0;
	t1 = (t_byte*)s1;
	t2 = (t_byte*)s2;
	if (n == 0)
		return (0);
	while (i < n - 1 && t1[i] != '\0')
	{
		if (t1[i] != t2[i])
			return (t1[i] - t2[i]);
		i++;
	}
	return (t1[i] - t2[i]);
}
