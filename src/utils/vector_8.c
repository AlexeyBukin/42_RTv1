#include "rt.h"

t_vec		vec_to_srgb(t_vec v)
{
	v.x = pow(v.x, 1 / 2.2);
	v.y = pow(v.y, 1 / 2.2);
	v.z = pow(v.z, 1 / 2.2);
	return (v);
}
